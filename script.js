const button = document.querySelector('#open-sideBar')

button.addEventListener('click', closed)
function closed() {
  const closed = document.querySelector()
  closed.classList.remove('closed')
};

const body = document.querySelector('body')
body.addEventListener('keydown', open)
function open(event) {
  var tecla = event.keyCode;
  if(tecla === 27){
    const open = document.querySelector()
  }
}